public class QuilometragemException extends Exception{

    public QuilometragemException() {
    }

    public QuilometragemException(String message) {
        super(message);
    }

    public QuilometragemException(String message, Throwable cause) {
        super(message, cause);
    }

    public QuilometragemException(Throwable cause) {
        super(cause);
    }

    public QuilometragemException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
    
    
    
}
