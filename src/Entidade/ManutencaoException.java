
package Entidade;

public class ManutencaoException extends Exception{

    public ManutencaoException() {
    }

    public ManutencaoException(String message) {
        super(message);
    }

    public ManutencaoException(String message, Throwable cause) {
        super(message, cause);
    }

    public ManutencaoException(Throwable cause) {
        super(cause);
    }

    public ManutencaoException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
    
    
    
    
}
