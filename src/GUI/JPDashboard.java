    private void CarregaTabelaTodasManutencao(Veiculo veiculo) throws Exception {
       
        this.JTManutencaoTodas.deleteRows();
              
        DefaultTableModel dtm = (DefaultTableModel) this.JTManutencaoTodas.getModel();
        
        List<Object> list = new ArrayList<>();
        list = this.facdash.CarregaTabelaTodasManutencao(veiculo);
        
        for (Object object : list) {
            
            dtm.addRow((Object[]) object);
            
        }
        
        int qtdrows = this.JTManutencaoTodas.getRowCount();      
        this.JLTodasManutencao.setText("Todas" + " ("+ qtdrows+")");
        
        this.JTManutencaoTodas.ColorRow(JTManutencaoTodas, new String[]{"E","C","D","D","E","E","D","C"});
        
        
    }
    
