      private void CarregaComboPlaca() {

        try {

            Veiculo v = new Veiculo();
            v.setID(-1);
            v.setPlaca("");

            List<Veiculo> listv = new ArrayList<>();
            listv = this.facvei.Consultar(v);

            Collections.sort(listv, Comparator.comparing(Veiculo::getModelo));

            this.jComboBoxPlaca.addItem("-- Selecione --");
       
            this.jComboBoxPlaca.setSelectedIndex(0);
       
            int[] listaInt = new int[listv.size() + 1];

            for (int x = 1; x <= listv.size(); x++) {
                listaInt[x] = listv.get(x - 1).getID();
                this.jComboBoxPlaca.addItem(listv.get(x - 1).getPlaca());
                        }

            this.jComboBoxPlaca.setCodCombo(listaInt);
     

        } catch (QuilometragemException | ManutencaoException | VeiculoException qe) {
            JOptionPane.showMessageDialog(null, qe.getMessage(), "Aviso", JOptionPane.WARNING_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }
    private void carregaDashBoard(){
    
        try {

            if (this.jComboBoxPlaca.getSelectedIndex() == 0) {

                throw new VeiculoException("Selecione a placa do veÃ­culo.");
            }

            this.JPDashboardTop.setVisible(false);
            this.JPMainContent.setVisible(false);

            Thread tr1 = new Thread() {
                @Override
                public void run() {
                    JLCarregando.setVisible(true);
                }
            };

            Thread tr2 = new Thread() {
                @Override
                public void run() {

                    try {
                        CarregaDasboard();
                        JPDashboardTop.setVisible(true);
                        JPMainContent.setVisible(true);
                        JLCarregando.setVisible(false);
                    } catch (VeiculoException qe) {
                        JLCarregando.setVisible(false);
                        JOptionPane.showMessageDialog(null, qe.getMessage(), "Aviso", JOptionPane.WARNING_MESSAGE);
                    } catch (Exception ex) {
                        JLCarregando.setVisible(false);
                        JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                    }
                }

            };

            tr1.start();
            tr2.start();

        } catch (VeiculoException qe) {
            JOptionPane.showMessageDialog(null, qe.getMessage(), "Aviso", JOptionPane.WARNING_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
        
    
    }
    
    
