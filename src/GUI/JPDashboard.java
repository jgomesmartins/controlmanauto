    private void CarregaVeiculo(String placa) throws Exception, VeiculoException {

        Veiculo vi = new Veiculo();
        vi.setPlaca(placa);

        List<Veiculo> listv = new ArrayList<>();

        listv = this.facvei.Consultar(vi);

        if (listv.size() > 0) {

            this.veiculo.setID(listv.get(0).getID());
            this.veiculo.setAnoFabricacao(listv.get(0).getAnoFabricacao());
            this.veiculo.setProprietario(listv.get(0).getProprietario());
            this.veiculo.setKmInicial(listv.get(0).getKmInicial());
            this.veiculo.setDataCompra(listv.get(0).getDataCompra());
            this.veiculo.setPlaca(listv.get(0).getPlaca());

        } else {
            throw new VeiculoException("VeÃ­culo nÃ£o encontrado no cadastro do sistema.");
        }

    }

    private void CarregaDadosGerais(Veiculo v) throws Exception {

        List<Object> listobj = new ArrayList<>();

        listobj = this.facdash.CarregaDadosGerais(v);

        if (listobj.size() > 0) {

            this.JLProprietario.setText((String) ((Object[]) listobj.get(0))[0]);
            this.JLKMCompra.setText((String) ((Object[]) listobj.get(0))[2]);
            this.JLDataCompra.setText((String) ((Object[]) listobj.get(0))[1]);
            this.JLKMAtual.setText((String) ((Object[]) listobj.get(0))[3]);
            this.JLValorTotal.setText((String) ((Object[]) listobj.get(0))[4]);
            this.JLDataKmAtual.setText((String) ((Object[]) listobj.get(0))[5]);

            String kminicial = (String) ((Object[]) listobj.get(0))[3];
            String kmatual = (String) ((Object[]) listobj.get(0))[2];

            kminicial = kminicial.replace(".", "");
            kmatual = kmatual.replace(".", "");

           
            int kmrodado = Integer.parseInt(kminicial) - Integer.parseInt(kmatual);
            this.JLTotalKMRodado.setText(Utils.FormartDecimal("###,###", kmrodado));

            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate d1 = LocalDate.parse(this.JLDataCompra.getText().replace("-", "/"), formatter);
            LocalDate d2 = LocalDate.parse(this.JLDataKmAtual.getText().replace("-", "/"), formatter);
            LocalDate dataini = LocalDate.of(d1.getYear(), d1.getMonthValue(), d1.getDayOfMonth());
            LocalDate datafim = LocalDate.of(d2.getYear(), d2.getMonthValue(), d2.getDayOfMonth());
            long diferencaEmDias = ChronoUnit.DAYS.between(dataini, datafim);
            long diferencaEmMes = ChronoUnit.MONTHS.between(dataini, datafim);
            this.JLQtdMeses.setText(Long.toString(diferencaEmMes));

            if (kmrodado > 0) {

                double mediakmmes = Double.parseDouble(this.JLTotalKMRodado.getText().replace(".", "")) / Double.parseDouble(this.JLQtdMeses.getText());
                
                if (Integer.parseInt(this.JLQtdMeses.getText())>0) {
                   this.JLMediaKMMes.setText(Utils.FormartDecimal("###,###", mediakmmes)); 
                }               

                double totalkm = Long.parseLong(this.JLTotalKMRodado.getText().replace(".", ""));

                if (totalkm > 0 && diferencaEmDias >0) {
                    double mediakmdia = totalkm / diferencaEmDias;
                    this.JLMediaKMDia.setText(Utils.FormartDecimal("##.##", mediakmdia));
                }
                
  

            } else {
                this.JLMediaKMDia.setText("0");
                this.JLMediaKMMes.setText("0");

            }

        } else {

            this.JLProprietario.setText("");
            this.JLKMCompra.setText("");
            this.JLDataCompra.setText("");
            this.JLKMAtual.setText("");

            this.JLTotalKMRodado.setText("");
            this.JLValorTotal.setText("");
            this.JLDataKmAtual.setText("");
            this.JLMediaKMMes.setText("");

        }

    }

